#include <iostream> //预编译指令，包含标准头文件

using namespace std; // 使用标准名空间

int main() { //主函数的函数头，全局惟一

	cout << "Hello!" << endl; // 标准输出对象，在输出设备（一般为屏幕）上输出一行文本；连续的双左尖括号表示向左侧输出对象提供右侧的信息；双引号内为字符串内容；‘\n’为转义字符，表示换行；分号表示语句结束

	cout << "Welcome to c++!" << endl;
	return 0; //函数结束执行，程序退出
} // 函数体，花括号内为语句序列
